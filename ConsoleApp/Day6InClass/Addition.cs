using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Day6InClass
{
    internal static class Addition
    {
        public static int AddNumbers(int a, int b) { return a + b; }

        public static double AddNumbers(double a, double b) {  return a + b; }

        public static int AddNumbers(int a, int b, int c) { return a + b + c; }
    }
